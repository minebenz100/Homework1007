public class Store {
    public string name;
    public double number;
    public string owner_name;
    public double registered_values;

    public Store(string name,
     double number,
     string owner_name,
     double registered_values) {
        this.name = name;
        this.number = number;
        this.owner_name = owner_name;
        this.registered_values = registered_values;
    }
    
}